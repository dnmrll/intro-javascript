/*
    OPERACIONES DE COMPARACION
*/

// ==  : igualdad
// === : para comparar igualdad en valor y en tipo de dato
// !=  : es diferente a, true o false 
// !== : es diferente en valor y tipo de dato 

// >   : mayor que
// <   : menor que
// >=  : mayor e igual que
// <=  : menor e igual que

const a = 10
const b = 20
const c = '10'

console.log(a == b) // false
console.log(a === c) // false
console.log(a == c) // true
console.log(a != c) // falso -> si se interpreta como iguales
console.log(a !== c) // true -> porque es diferente en valor y tipo de dato